#include <stdio.h>
#include <stdlib.h>
#include <time.h>
/**
 * main - Prints if the last digit of n is 0, 5+ or 6-
 *
 * Return: Always 0 (Success)
 */

int main(void)
{
        char letter = 'a';
        while (letter <= 'z')
        {
                putchar(letter);
                letter++;
        }
        putchar('\n');
    return (0);
}
